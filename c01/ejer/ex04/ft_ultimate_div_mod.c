void	ft_ultimate_div_mod(int *a, int *b)
{
	int div;
	int res;
	int x;

	x=*a;

	if( x == 0)
	{
		*a = 1;
		*b = 0;
	}
	else
	{
		div = ( *a / *b);
		res = ( *a % *b);
		*a = div;
		*b = res;
	}
}
