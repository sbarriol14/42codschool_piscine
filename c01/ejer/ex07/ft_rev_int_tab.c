void	ft_rev_int_tab(int *tab, int size)
{
	int newt[size];
	int i;
	int j;

	if(size > 0)
	{
		i = size - 1;
		j = 0;
		while(i >= 0)
		{
			newt[j] = tab[i];
			i--;
			j++;
		}
		i = 0;
		while(i < size)
		{
			tab[i] = newt[i];
			i++;
		}	
	}
	
}
