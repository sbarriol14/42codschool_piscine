int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb == 4)
		return (2);
	if ((nb <= 0) || (nb > 2147483647))
		return (0);
	while (i < (nb / 2) && i < 48000)
	{
		if ((i * i) == nb)
			return (i);
		i++;
	}
	return (0);
}
